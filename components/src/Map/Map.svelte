<script lang="ts">
	import maplibre, { type StyleSpecification, type Map } from 'maplibre-gl';
	import { setContext, onMount, onDestroy, type Snippet } from 'svelte';
	import { SWRDataBaseLight } from '../MapStyle';
	import { dev } from '$app/environment';

	interface Location {
		lat: number;
		lng: number;
		zoom: number;
	}

	interface MapProps {
		style?: StyleSpecification | string;
		minZoom?: number;
		maxZoom?: number;
		zoom?: number;
		center?: maplibre.LngLat;
		pitch?: number;
		bearing?: number;
		loading?: boolean;
		allowRotation?: boolean;
		initialLocation?: Location;
		options?: any;
		children?: Snippet;
	}

	let {
		children,
		options,
		style = SWRDataBaseLight,
		minZoom = 0,
		maxZoom = 14.99,
		zoom = $bindable(),
		center = $bindable(),
		pitch = $bindable(),
		bearing = $bindable(),
		loading = $bindable(true),
		allowRotation = false,
		initialLocation = { lat: 51.5, lng: 10, zoom: 5 }
	}: MapProps = $props();

	let container: HTMLElement;

	let ctx: any = $state({
		map: undefined
	});

	setContext('ctx', ctx);

	onMount(() => {
		ctx.map = new maplibre.Map({
			container,
			style,
			minZoom,
			maxZoom,
			attributionControl: false,
			center: [initialLocation.lng, initialLocation.lat],
			zoom: initialLocation.zoom,
			...options
		});

		if (!allowRotation) {
			ctx.map.touchZoomRotate.disableRotation();
		}

		ctx.map.on('load', () => {
			zoom = ctx.map.getZoom();
			center = ctx.map.getCenter();
			pitch = ctx.map.getPitch();
			bearing = ctx.map.getBearing();
		});

		ctx.map.on('moveend', () => {
			zoom = ctx.map.getZoom();
			center = ctx.map.getCenter();
			pitch = ctx.map.getPitch();
			bearing = ctx.map.getBearing();
		});
	});

	onDestroy(async () => {
		if (ctx.map) ctx.map.remove();
	});

	$effect(() => {
		if (ctx.map) ctx.map.setStyle(style);
	});
</script>

<div bind:this={container} class="container" data-testid="map-container">
	{#if children}
		{@render children()}
	{/if}
	{#if dev}
		<div class="debug">
			{JSON.stringify({ ...center, zoom })}
		</div>
	{/if}
</div>

